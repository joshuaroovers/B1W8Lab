function addition(num1, num2){
    a = num1 + num2;
document.write(num1 + " + " + num2 + " = " + a + "<br>")
}

function substraction(num1, num2){
    s = num1 - num2;
    document.write(num1 + " - " + num2 + " = " + s + "<br>")
}

function multiplication(num1, num2){
    m = num1 * num2;
    document.write(num1 + " * " + num2 + " = " + m + "<br>")
}

function division(num1, num2){
    d = num1 / num2;
    document.write(num1 + " / " + num2 + " = " + d + "<br>")
}

function increment(num1){
    i = num1 + 1;
    document.write(num1 + " + 1" + " = " + i + "<br>")
}

function decrement(num1){
    d = num1 - 1;
    document.write(num1 + " - 1" + " = " + d + "<br>")
}


